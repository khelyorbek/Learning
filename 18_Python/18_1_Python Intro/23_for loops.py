# Python
# Behaves similar to 'for of' loop
# No equivalent to for loop from JS
colors = ['red','orange','yellow','green']
for color in colors:
    print(color)


for char in "ABCDEFGHIJKLMNOPQRSTUVWXYZ":
    print(char)

# if you want to repeat something 5 times you can 
# just create something that has 5 items and loop
# over it
for i in 'abcde':
    print("Looping 5 times!")

# you can also use range() function
for num in range(5): # makes [0,1,2,3,4]
    print(num)
# ^ much better than typing in trad for loop in JS


